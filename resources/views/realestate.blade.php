                                          <p style="color:black">Real estate trading is the wild side of real estate investment. Like day traders, who are distinct from buy-and-hold investors, real estate traders are an entirely different breed from buy-and-rent landlords. Real estate traders buy properties with the intention of holding them for a short period of time, often no more than three to four months, after which they hope to sell them for a profit. This technique is also called flipping properties and is based on buying properties that are either significantly undervalued or in a very hot market.</p>
                                          <p style="color:black">As property flippers we often forgo putting any money into a house for improvements; the investment has to have the intrinsic value to turn a profit without alteration or we won’t consider it. Flipping in this manner is a short-term cash investment. To take advantage of potentially large returns, We have to have cash on hand, as traditional financing doesn’t generally work for this type of transaction. </p>

                                          <p style="color:black">A second class of property flipper also exists. These investors make their money by buying reasonably priced properties and adding value by renovating them. This can be a longer-term investment depending on the extent of the improvements. The limiting feature of this investment is that it is time intensive and often only allows investors to take on one or two properties at a time hence we try to avoid properties with high maintenance cost and focus more on properties with high undervalue.</p>
